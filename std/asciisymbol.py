def asciiImage(inp):
    if inp == '':
        return ''
    row = 9

    content = ""
    
    # convert to upper case
    try:
        inp = inp.upper()
    except:
        pass

    # loop string
    for r in range(row):
        for c in inp:
            # get the symbol
            out_symbol = symbolDict.get(c, symbolDict[""])(r)
            content += out_symbol
        content += "<br />"
    return content