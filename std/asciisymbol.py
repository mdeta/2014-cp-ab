def asciiImage(inp):
    """
    字串轉成圖案, 回傳內容為 HTML 格式
    """
    if inp == '':
        return ''
    row = 9

    content = ""
    
    # convert to upper case
    try:
        inp = inp.upper()
    except:
        pass

    # loop string
    for r in range(row):
        for c in inp:
            # get the symbol
            out_symbol = symbolDict.get(c, symbolDict[""])(r)
            content += out_symbol
        content += "<br />"
    return content
